# for loop with ranges

for i in range(5):
    print(i) 

    #prints 0 to 4

print(".....")

for i in range(10,20):
    print(i)

    # prints 10 to 19

print(".....")

for i in range(10, 20, 2):
    print(i)

    # prints 10 to 19 with the range of 2 (10, 12, 14, 16, 18)

print(".....")

# negative steps

for i in range(10, 0, -1):
    print (i)

    # prints 10 to 1

print(".....")

for i in range(20, 10, -2):
    print(i)

    # prints 20 to 12 with step of 2

    